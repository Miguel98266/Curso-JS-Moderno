let resultado;

//PI
resultado = Math.PI;

//Redondear
resultado = Math.round(2.8);

//Redondear hacia arriba
resultado = Math.ceil(2.1);

//Redondea hacia abajo
resultado = Math.floor(2.9);

//Raiz cuadrada
resultado = Math.sqrt(144);

//Valor absoluto
resultado = Math.abs(-200);

//Potencia
resultado = Math.pow(8, 3);

